package day04_dataCasting;

public class Q02_Narrowing {
    public static void main(String[] args) {

        int sayi4=-350;
        byte sayi2= (byte) sayi4;
        System.out.println(sayi2);

        double sayi1=2.5;
        int sayi3= (int) sayi1;
        System.out.println(sayi3);

        int s1=150;
        byte s2= (byte) s1;
        System.out.println(s2);

    }
}

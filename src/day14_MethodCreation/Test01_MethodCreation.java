package day14_MethodCreation;

public class Test01_MethodCreation {
    public static void main(String[] args) {

        carpma(2,3);
        bolme(8,4);



    }

    public static void bolme(int i, int i1) {
        System.out.println(i/i1);

    }

    public static void carpma(int i, int i1) {
        System.out.println(i*i1);
    }
}

package day02_Variables;

public class Q01_Swap {
    public static void main(String[] args) {
        // swap ornegi//
        int s5=10;
        int s6=20;
        int temp=s5; //temp 10 oldu//
        s5=s6; //s5 20 oldu//
        s6=temp;
        System.out.println("s5:"+ s5 + " , s6:"+ s6);

        int s7= 40;
        int s8= 50;
        int tem =s7;
        s7=s8;
        s8=tem;
        System.out.println("s7:" + s7 + ", s8:" + s8);

        int s9= 30;
        int s0= 80;
        int te= s9;
        s9=s0;
        s0=te;
        System.out.println("s9:"+ s9 + " , s0:" + s0);

    }
}
